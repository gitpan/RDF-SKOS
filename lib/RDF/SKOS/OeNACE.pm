our %SKOS;
sub _initialize {
    <DATA>; # skip first
    while ($_ = <DATA>) {
	last if /----/;
	$csv->parse ($_);
	my @fs = $csv->fields ();
	$SKOS{$fs[1]} = [ @fs[2,3] ];
    }
    <DATA>; # skip first
    while ($_ = <DATA>) {
	$csv->parse ($_);
	my @fs = $csv->fields ();
	push @{ $SKOS{$fs[1]} }, $fs[3];
    }

    foreach my $k (keys %SKOS ) { #-- internal consistency check
	die "inconsistent data" unless scalar @{ $SKOS{$k} } == 3;
    }
}


sub new {
    my $class = shift;
    _initialize unless %SKOS;
    my $self = bless {}, $class;
    return $self;
}

=pod

=head2 Methods

See also L<RDF::SKOS>.

=over

=item B<concept>

Different to the superclass, this method is read-only.

=cut

sub concept {
    my $self = shift;
    my $id   = shift;
    return new RDF::SKOS::Concept ($self, $id) if $SKOS{$id};
}

sub concepts {
    my $self = shift;
    return map { new RDF::SKOS::Concept ($self, $_) } keys %SKOS;
}

=pod

=item B<schemes>

As OeNACE does not have any explicit scheme, this will return an empty list.

=item B<scheme>

As there are no scheme, we will die here.

=cut

sub scheme { die "no schemeing"; }

=pod

=item B<topConcepts>

While there are no schemes, there are top concepts in OeNACE. These
are those with a single letter ID (A, B, C, ...) and these will be
returned. Any scheme parameter will be ignored.

=cut

sub topConcepts {
    my $self = shift;
    return 
	map  { new RDF::SKOS::Concept ($self, $_) }
        grep { $_ =~ /^[A-Z]$/}
        keys %SKOS;
}

sub prefLabels {
    my $self = shift;
    my $id   = shift;
    return
	[ $SKOS{$id} [1], 'de' ],
	[ $SKOS{$id} [2], 'en' ];
}

sub altLabels      { () }

sub hiddenLabels {
    my $self = shift;
    my $id   = shift;
    return [ $id, undef ];
}

sub notes          { () }
sub scopeNotes     { () }
sub definitions    { () }
sub examples       { () }
sub historyNotes   { () }
sub editorialNotes { () }
sub changeNotes    { () }


sub narrower {
    my $self = shift;
    my $id   = shift;
    if ($id =~ /^[A-Z]$/) {
	return
	    map { bless { id => $_, skos => $self }, 'RDF::SKOS::Concept' }
	    grep { /$id..$/ }
	    keys %SKOS;
    } else {
	return
	    map { bless { id => $_, skos => $self }, 'RDF::SKOS::Concept' }
	    grep { /$id.$/ }
	    keys %SKOS;
    }
}

=pod

=item B<narrowerTransitive>

B<NOTE>: At the moment this does not honor transitivity.

=cut

sub narrowerTransitive {
    my $self = shift;
    $self->narrower (@_);
}

sub broader {
    my $self = shift;
    my $id   = shift;

    if ($id =~ /^([A-Z])..$/) {
	return bless { id => $1, skos => $self }, 'RDF::SKOS::Concept';
    } elsif ($id =~ /^[A-Z]$/) {
	return ();
    } elsif ($id =~ /([A-Z].*).$/) {
	return bless { id => $1, skos => $self }, 'RDF::SKOS::Concept';
    }
}

=pod

=item B<broaderTransitive>

B<NOTE>: At the moment this does not honor transitivity.

=cut

sub broaderTransitive {
    my $self = shift;
    $self->broader (@_);
}

=pod

=back

